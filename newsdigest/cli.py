MENU_OPTIONS = [
    ("1", "Send digest now",           action_send_now),
    ("2", "Preview digest (dry run)",  action_preview),
    ("3", "Force re-send digest",      action_send_force),
    ("4", "Change news sources",       action_change_sources),
    ("5", "Change email address",      action_change_email),
    ("6", "Change app password",       action_change_password),
    ("7", "Change delivery schedule",  action_change_schedule),
    ("8", "View status",              action_status),
    ("9", "Uninstall",                action_uninstall),
    ("0", "Exit",                     None),
]


def show_menu() -> None:
    """Display the interactive menu and handle user choices in a loop."""
    # Ensure stdin reads from terminal if piped
    if not sys.stdin.isatty():
        try:
            sys.stdin = open("/dev/tty", "r")
        except OSError:
            pass

    while True:
        _clear()
        _header("News Digest")

        # ── Status bar ──────────────────────────────────────────
        env = _read_env()
        email = env.get("SMTP_EMAIL", "")
        sources_raw = env.get("SELECTED_SOURCES", "")
        source_count = len([s for s in sources_raw.split(",") if s.strip()])
        sched = env.get("SCHEDULE_TIME", "")

        if email:
            parts = [email]
            if source_count:
                parts.append(f"{source_count} source{'s' if source_count != 1 else ''}")
            if sched:
                parts.append(f"Daily at {sched}")
            else:
                parts.append("Manual delivery")
            print(f"  {DIM}{'  ·  '.join(parts)}{NC}")

        # ── Send ────────────────────────────────────────────────
        print()
        print(f"  {CYAN}── Send {'─' * (W - 9)}{NC}")
        print(f"    {BOLD}[1]{NC}  Send digest now")
        print(f"    {BOLD}[2]{NC}  Preview digest {DIM}(dry run){NC}")
        print(f"    {BOLD}[3]{NC}  Force re-send")

        # ── Settings ────────────────────────────────────────────
        print()
        print(f"  {CYAN}── Settings {'─' * (W - 13)}{NC}")
        print(f"    {BOLD}[4]{NC}  News sources")
        print(f"    {BOLD}[5]{NC}  Email address")
        print(f"    {BOLD}[6]{NC}  App password")
        print(f"    {BOLD}[7]{NC}  Delivery schedule")

        # ── System ──────────────────────────────────────────────
        print()
        print(f"  {CYAN}{'─' * W}{NC}")
        print(f"    {BOLD}[8]{NC}  Status")
        print(f"    {RED}[9]  Uninstall{NC}")
        print(f"    {DIM}[0]  Exit{NC}")

        print()
        choice = input(f"  {BOLD}▸{NC} ").strip()

        if choice == "0":
            _clear()
            print()
            print(f"  {DIM}👋 Goodbye!{NC}")
            print()
            break

        matched = False
        for key, _, action in MENU_OPTIONS:
            if choice == key and action is not None:
                action()
                matched = True
                break

        if not matched and choice != "0":
            _warn(f"Invalid choice: '{choice}'. Please enter a number 0–9.")
            _pause()


def main() -> None:
    """Entry point for the interactive CLI menu."""
    show_menu()


if __name__ == "__main__":
    main()
