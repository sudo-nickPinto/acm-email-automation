# =============================================================================
# cli.py — Interactive CLI Menu for News Digest
# =============================================================================
#
# When a user types `news-digest` with no arguments, this module powers
# the interactive menu that appears.  It lets subscribed users manage
# their digest without needing to remember command-line flags.
#
# Menu options:
#   1) Send digest now
#   2) Preview digest (dry run)
#   3) Change news sources
#   4) Change email address
#   5) Change app password
#   6) Change delivery schedule
#   7) View current status
#   8) Uninstall
#   9) Exit
#
# Dependencies:
#   newsdigest.config   — current settings
#   newsdigest.sources   — source registry
#   newsdigest.scheduler — schedule management
#   setup_wizard         — reuses formatting helpers
# =============================================================================

"""
Interactive CLI menu for managing the News Digest installation.
"""

import importlib
import getpass
import os
import re
import subprocess
import sys
from pathlib import Path

from newsdigest.paths import venv_python_path

# ---------------------------------------------------------------------------
# Terminal formatting (matches style in setup_wizard.py)
# ---------------------------------------------------------------------------

BOLD = "\033[1m"
DIM = "\033[2m"
GREEN = "\033[0;32m"
YELLOW = "\033[1;33m"
RED = "\033[0;31m"
CYAN = "\033[0;36m"
BLUE = "\033[0;34m"
WHITE = "\033[1;37m"
NC = "\033[0m"

PROJECT_ROOT = Path(__file__).resolve().parent.parent
ENV_FILE = PROJECT_ROOT / ".env"
VENV_PYTHON = venv_python_path(PROJECT_ROOT)
WINDOWS_WRAPPER = Path.home() / "AppData" / "Local" / "NewsDigest" / "bin" / "news-digest.cmd"

W = 55  # Display width for divider lines


def _clear() -> None:
    """Clear the terminal screen (only when connected to a real terminal)."""
    if sys.stdout.isatty():
        print("\033[2J\033[H", end="", flush=True)


def _header(title: str) -> None:
    """Display the main menu banner with prominent styling."""
    print()
    print(f"  {CYAN}{'━' * W}{NC}")
    print(f"  {WHITE}  📰  {title}{NC}")
    print(f"  {CYAN}{'━' * W}{NC}")


def _banner(text: str) -> None:
    """Display a sub-screen header."""
    print()
    print(f"  {CYAN}{'─' * W}{NC}")
    print(f"  {BOLD}  {text}{NC}")
    print(f"  {CYAN}{'─' * W}{NC}")
    print()


def _success(text: str) -> None:
    print(f"  {GREEN}✔{NC}  {text}")


def _warn(text: str) -> None:
    print(f"  {YELLOW}⚠{NC}  {text}")


def _fail(text: str) -> None:
    print(f"  {RED}✘{NC}  {text}")


def _info(text: str) -> None:
    print(f"  {BLUE}ℹ{NC}  {text}")


def _pause() -> None:
    """Wait for user to press Enter before returning to the menu."""
    print()
    input(f"  {DIM}Press Enter to continue ↵{NC} ")


def _reload_config():
    """Force-reload config module so it picks up .env changes."""
    import newsdigest.config
    # Clear cached env vars so load_dotenv re-reads the file
    for key in ("SMTP_EMAIL", "SMTP_APP_PASSWORD", "RECIPIENT_EMAIL",
                "SELECTED_SOURCES", "SCHEDULE_TIME"):
        os.environ.pop(key, None)
    importlib.reload(newsdigest.config)
    return newsdigest.config


def _protect_env_file() -> None:
    """Restrict .env readability on POSIX systems."""
    if os.name == "nt" or not ENV_FILE.exists():
        return

    try:
        ENV_FILE.chmod(0o600)
    except OSError:
        _warn("Couldn't tighten permissions for .env.")


def _setup_command() -> str:
    """Return the preferred user-facing setup command for this OS."""
    if os.name == "nt":
        return "news-digest setup"
    return "./start.sh"


def _read_env() -> dict[str, str]:
    """Read the current .env file into a dict."""
    env = {}
    if ENV_FILE.exists():
        for line in ENV_FILE.read_text().splitlines():
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                key, _, value = line.partition("=")
                env[key.strip()] = value.strip()
    return env


def _write_env(env: dict[str, str]) -> None:
    """Write the env dict back to the .env file."""
    lines = ["# News Digest Configuration",
             f"# Generated by setup wizard — re-run {_setup_command()} to change"]
    for key in ("SMTP_EMAIL", "SMTP_APP_PASSWORD", "RECIPIENT_EMAIL",
                "SELECTED_SOURCES", "SCHEDULE_TIME"):
        if key in env:
            lines.append(f"{key}={env[key]}")
    ENV_FILE.write_text("\n".join(lines) + "\n")
    _protect_env_file()


